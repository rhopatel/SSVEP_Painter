
from tkinter import *
import tkinter

def main():
    print("hello")
    # create root window
    root = Tk()


    # root window title and dimension
    root.title("SSVEP_demo")
    # Set geometry (widthxheight)
    root.geometry('350x200')
    
    # all widgets will be here
    # Execute Tkinter
    root.mainloop()

if __name__ == "__main__":
    main()